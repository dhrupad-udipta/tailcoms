<script>
export default {
  data(){
    return{
      open:false,
      open_pages_tab:false,
    }
  },
  methods:{
    hideDropDown(){
      this.open = false;
      this.open_pages_tab = false;
    },
    toggleComponents(){
        if(this.open){
          this.open = false;
        }else{
          this.open = true;
          this.open_pages_tab = false;
        }
    },
    togglePages(){
        if(this.open_pages_tab){
          this.open_pages_tab = false;
        }else{
          this.open_pages_tab = true;
          this.open = false;
        }
    }
  }
}
</script>
